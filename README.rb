= WikiNG plugin for Redmine

This is a fork of WikiNG plugin by Andriy Lesyuk http://projects.andriylesyuk.com/projects/wiking/wiki
The aim of the fork is to provide Redmine 2.0 support and small enhancements.

The WikiNG plugin for Redmine and ChiliProject is aimed to extend Redmine Wiki syntax by making it more developer-ready, more emotional and social...
